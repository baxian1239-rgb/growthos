export interface QuestionOption {
  text: string;
  score: number;
  label: string;
}

export interface Question {
  id: number;
  category: string;
  question: string;
  options: QuestionOption[];
  engine: string;
  title: string;
}

function option(text: string, score: number): QuestionOption {
  return {
    text,
    score,
    label: text,
  };
}

function question(id: number, category: string, text: string, options: string[]): Question {
  return {
    id,
    category,
    question: text,
    engine: category,
    title: text,
    options: options.map((item, index) => option(item, index + 1)),
  };
}

